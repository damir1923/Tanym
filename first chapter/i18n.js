// –ü–æ–ª—É—á–∏—Ç—å —Ç–µ–∫—É—â–∏–π —è–∑—ã–∫
function getCurrentLanguage() {
    return localStorage.getItem('language') || 'ru';
}

// –£—Å—Ç–∞–Ω–æ–≤–∏—Ç—å —è–∑—ã–∫
function setLanguage(lang) {
    if (translations[lang]) {
        localStorage.setItem('language', lang);
        window.location.reload();
    }
}

// –ü–æ–ª—É—á–∏—Ç—å –ø–µ—Ä–µ–≤–æ–¥ –ø–æ –∫–ª—é—á—É
function t(key) {
    const lang = getCurrentLanguage();
    if (translations[lang] && translations[lang][key]) {
        return translations[lang][key];
    }
    // Fallback to Russian if translation not found
    if (translations['ru'] && translations['ru'][key]) {
        return translations['ru'][key];
    }
    return key;
}

// –ü–µ—Ä–µ–≤–æ–¥–∏—Ç—å –≤—Å–µ —ç–ª–µ–º–µ–Ω—Ç—ã
function translateAll() {
    // –ü–µ—Ä–µ–≤–æ–¥–∏–º –≤—Å–µ —ç–ª–µ–º–µ–Ω—Ç—ã —Å data-i18n –∞—Ç—Ä–∏–±—É—Ç–æ–º
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
        const key = el.getAttribute('data-i18n');
        const text = t(key);
        el.textContent = text;
    });

    // –ü–µ—Ä–µ–≤–æ–¥–∏–º placeholder'—ã
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
        const key = el.getAttribute('data-i18n-placeholder');
        el.placeholder = t(key);
    });

    // –ü–µ—Ä–µ–≤–æ–¥–∏–º title
    document.querySelectorAll('[data-i18n-title]').forEach(function (el) {
        const key = el.getAttribute('data-i18n-title');
        el.title = t(key);
    });

    // –ü–µ—Ä–µ–≤–æ–¥–∏–º value
    document.querySelectorAll('[data-i18n-value]').forEach(function (el) {
        const key = el.getAttribute('data-i18n-value');
        el.value = t(key);
    });
}

